  # POST /resource
  def create
    meta_type = params[:account][:meta_type]
    meta_type_params = params[:account][meta_type]
  
    params[:account].delete(:meta_type)
    params[:account].delete(meta_type)
  
    build_resource(sign_up_params)
  
    @doctor = meta_type.camelize.constantize
    Doctor.new(params[Doctor.to_s.underscore.to_sym])
    resource.meta = Doctor.new(meta_type_params)
  
    # first check if child intance is valid
    # cause if so and the parent instance is valid as well
    # it's all being saved at once
    valid = resource.valid?
    valid = resource.meta.valid? && valid  

    # customized code end

    if valid && resource.save    # customized code
      if resource.active_for_authentication?
        set_flash_message :notice, :signed_up if is_navigational_format?
        sign_in(resource_name, resource)
        respond_with resource, :location => redirect_location(resource_name, resource)
      else
        set_flash_message :notice, :inactive_signed_up, :reason => inactive_reason(resource) if is_navigational_format?
        expire_session_data_after_sign_in!
        respond_with resource, :location => after_inactive_sign_up_path_for(resource)
      end
    else
      clean_up_passwords(resource)
      respond_with_navigational(resource) { render :new }
    end
  end


